#Print a integer value
my_int = 4
print(my_int)

#Print a float value
my_float = 4.0
print(my_float)

#Print a String
my_string = "hello word"
print(my_string)

#Print a boolean value
my_bool = True
print(my_bool)

#Print a None value
my_nothing = None
print(my_nothing)

#Print a f-string containing all previous values in one line i.e 1, 1.0, "hello"
print(f'{my_int}, {my_float}, {my_string}, {my_bool}, {my_nothing}')